package org.jumao.spider.beans;

import java.io.Serializable;

public class UserTraceInfo implements Serializable {

    private static final long serialVersionUID = 8822965560390351660L;
    private String            key;
    private String            bizCode;
    private String            userId;
    private String            companyId;
    private String            isOverseas;//false 中文站用户，true英文站用户
    private String            loginTime;
    private String            deviceType;
    private String            browseType;

    public UserTraceInfo() {

    }

    public UserTraceInfo(String key, String bizCode, String userId, String companyId, String loginTime, String userAgent, String deviceType, String browseType) {
        this.key = key;
        this.bizCode = bizCode;
        this.userId = userId;
        this.companyId = companyId;
        this.loginTime = loginTime;
        this.deviceType = deviceType;
        this.browseType = browseType;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getBizCode() {
        return bizCode;
    }

    public void setBizCode(String bizCode) {
        this.bizCode = bizCode;
    }

    public String getLoginTime() {
        return loginTime;
    }

    public void setLoginTime(String loginTime) {
        this.loginTime = loginTime;
    }

    public String getDeviceType() {
        return deviceType;
    }

    public void setDeviceType(String deviceType) {
        this.deviceType = deviceType;
    }

    public String getBrowseType() {
        return browseType;
    }

    public void setBrowseType(String browseType) {
        this.browseType = browseType;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getCompanyId() {
        return companyId;
    }

    public void setCompanyId(String companyId) {
        this.companyId = companyId;
    }

    public String getIsOverseas() {
        return isOverseas;
    }

    public void setIsOverseas(String isOverseas) {
        this.isOverseas = isOverseas;
    }
    
    

}
