package com.jumore.jmdata.common.exceptions;

import com.jumore.jmdata.enums.BaseExceptionEnum;

/** 
*校验异常类
* @author: luochao
* @since: 2016年7月4日  下午6:02:00
* @history:
*/
public class ValidationException extends BaseException {

    /**
     * 序列号
     */
    private static final long serialVersionUID = 2653145822151544474L;

    public ValidationException(String code, String message) {
        super(code, message);
    }

    public ValidationException(String code, String message, Throwable cause) {
        super(code, message, cause);
    }

    public ValidationException(BaseExceptionEnum baseExceptionEnum) {
        super(baseExceptionEnum);
    }

    public ValidationException(BaseExceptionEnum baseExceptionEnum, Throwable cause) {
        super(baseExceptionEnum, cause);
    }
}
