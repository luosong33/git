package org.jumao.bi.entites.trade.operate;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * Created by Administrator on 2017/5/3.
 */
public class LineChart implements Serializable {
    private static final long serialVersionUID = 4445236181854347736L;
    private String title;
    private String seriesName;  //  指标名称
    private String unit;  //  单位
    private BigDecimal[] seriesData;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSeriesName() {
        return seriesName;
    }

    public void setSeriesName(String seriesName) {
        this.seriesName = seriesName;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public BigDecimal[] getSeriesData() {
        return seriesData;
    }

    public void setSeriesData(BigDecimal[] seriesData) {
        this.seriesData = seriesData;
    }
}
