import org.jumao.bi.entites.trade.register.vo.MultiDimLine;
import org.jumao.bi.entites.trade.register.vo.MultiDimPie;

import java.io.Serializable;

public class MultiDimChartResp extends CommonResponse implements Serializable {

    private MultiDimPie pieChart = new MultiDimPie();
    private MultiDimLine lineChart = new MultiDimLine();



    public MultiDimPie getPieChart() {
        return pieChart;
    }

    public void setPieChart(MultiDimPie pieChart) {
        this.pieChart = pieChart;
    }

    public MultiDimLine getLineChart() {
        return lineChart;
    }

    public void setLineChart(MultiDimLine lineChart) {
        this.lineChart = lineChart;
    }
}
