package org.jumao.bi.entites.operate;

public class OrderInfo {
	private Integer goodsCateId;
	private String goodsCateName;
	private Integer goodsCateCnt;
	private double goodsCatePay;
	
	public Integer getGoodsCateId() {
		return goodsCateId;
	}
	public void setGoodsCateId(Integer goodsCateId) {
		this.goodsCateId = goodsCateId;
	}
	public String getGoodsCateName() {
		return goodsCateName;
	}
	public void setGoodsCateName(String goodsCateName) {
		this.goodsCateName = goodsCateName;
	}
	public Integer getGoodsCateCnt() {
		return goodsCateCnt;
	}
	public void setGoodsCateCnt(Integer goodsCateCnt) {
		this.goodsCateCnt = goodsCateCnt;
	}
	public double getGoodsCatePay() {
		return goodsCatePay;
	}
	public void setGoodsCatePay(double goodsCatePay) {
		this.goodsCatePay = goodsCatePay;
	}
	
	
}
