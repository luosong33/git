package org.jumao.bi.entites;

import java.io.Serializable;

public class CompanyInfo implements Serializable {

	private static final long serialVersionUID = 1L;
	// rowkey
	private String platformId;// 平台类型：1聚贸平台,2卖塑郎,3卖铜郎
	private String companyId;
	// rowkey
	private String companyName;
	private String industryId;// 主营行业Id
	private String industryName;// 名称
	private String type;// 公司性质
	private String status;// 企业状态：
	private String createTime;// 公司创建时间
	private String deleteFlag;// 删除标识:0 正常、 1删除

	public CompanyInfo() {
		super();
	}

	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getPlatformId() {
		return platformId;
	}

	public void setPlatformId(String platformId) {
		this.platformId = platformId;
	}

	public String getIndustryId() {
		return industryId;
	}

	public void setIndustryId(String industryId) {
		this.industryId = industryId;
	}

	public String getIndustryName() {
		return industryName;
	}

	public void setIndustryName(String industryName) {
		this.industryName = industryName;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getDeleteFlag() {
		return deleteFlag;
	}

	public void setDeleteFlag(String deleteFlag) {
		this.deleteFlag = deleteFlag;
	}

}
